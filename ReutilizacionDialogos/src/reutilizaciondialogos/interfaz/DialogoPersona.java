    private void jButtonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarActionPerformed
        String nombre = jTextFieldNombre.getText();
        String apellidos = jTextFieldApellidos.getText();
        int edad = Integer.parseInt(jTextFieldEdad.getText());
        //Checkeamos si es un alta o una modificación
        if (personaModificar == null)
        {
            Persona p = new Persona(nombre,apellidos,edad);
            logicaNegocio.altaPersona(p);
        }
        else
        {
            personaModificar.setNombre(nombre);
            personaModificar.setApellidos(apellidos);
            personaModificar.setEdad(edad);
        }
        dispose();
        
    }//GEN-LAST:event_jButtonAceptarActionPerformed

